#En este código aprenderemos el uso de los diferentes operadores aritmeticos y logicos.


a = 1
b = 2
print("a = " + str(a))
print("b = " + str(b))

#Operadores aritmeticos

print("Suma: a + b")
print(str(a+b))

print("Resta: a - b")
print(str(a-b))

print("Multiplicación: a * b")
print(str(a*b))

print("División: a / b")
print(str(a/b))

print("Exponente: b**2")
print(str(b**2))

#Operadores logicos

print("Igual: ==")
print("Mayor que: >")
print("Menor que: <")
print("Diferente de: !=")


